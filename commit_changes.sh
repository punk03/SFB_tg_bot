#!/bin/bash

# Скрипт для коммита всех изменений в репозиторий GitHub

# Цвета для вывода
GREEN="\033[0;32m"
RED="\033[0;31m"
YELLOW="\033[0;33m"
RESET="\033[0m"

echo -e "${GREEN}=== Коммит изменений в репозиторий GitHub ===${RESET}"

# Добавляем все файлы в индекс
echo -e "${YELLOW}1. Добавляем все измененные файлы в индекс...${RESET}"
git add .
echo -e "${GREEN}✅ Файлы добавлены${RESET}"

# Создаем коммит
echo -e "${YELLOW}2. Создаем коммит...${RESET}"
git commit -m "Добавлены ссылки на все ресурсы в футере каждого сообщения бота"
echo -e "${GREEN}✅ Коммит создан${RESET}"

# Отправляем изменения на GitHub
echo -e "${YELLOW}3. Отправляем изменения на GitHub...${RESET}"
git push
echo -e "${GREEN}✅ Изменения отправлены${RESET}"

echo -e "${GREEN}=== Все изменения успешно закоммичены и отправлены в репозиторий GitHub ===${RESET}" 